public class CalculatorModel {

    private String s0, s1, s2;

    public CalculatorModel()
    {
            this.setS0("");
            this.setS1("");
            this.setS2("");
    }

    public String getS0() {
        return s0;
    }

    public void setS0(String s0) {
        this.s0 = s0;
    }

    public String getS1() {
        return s1;
    }

    public void setS1(String s1) {
        this.s1 = s1;
    }

    public String getS2() {
        return s2;
    }

    public void setS2(String s2) {
        this.s2 = s2;
    }
}
